# Creating a Dictionary
# with Integer Keys
Dict = {1: 'Sabahul', 2: 'Welcome', 3: 'To', 4:'CU'}
print("\nDictionary with the use of Integer Keys: ")
print(Dict)

# Creating a Dictionary
# with Mixed keys
Dict = {'Name': 'Sabahul', 1: [1, 2, 3, 4]}
print("\nDictionary with the use of Mixed Keys: ")
print(Dict)
